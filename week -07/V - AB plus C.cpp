#include<bits/stdc++.h>
#define ll long long
using namespace std;
int main()
{
    int t; cin>>t;
    while(t--)
    {
       ll n; cin>>n;
       if(n==1)
       cout<<-1<<endl;
       else if(n<1000000)
       cout<<1<<" "<<1<<" "<<n-1<<endl;
       else if(n>=1000000 && n<10000000)
       {
        cout<<1000<<" "<<100<<" "<<n-100000<<endl;
       }
       else if(n>=10000000 && n<100000000)
       {
        cout<<1000<<" "<<1000<<" "<<n-1000000<<endl;
       }
       else if(n>=100000000 && n<1000000000)
       {
           cout<<1000<<" "<<10000<<" "<<n-10000000<<endl;
       }
       else if(n>=1000000000 && n<10000000000)
       {
           cout<<10000<<" "<<10000<<" "<<n-100000000<<endl;
       }
       else if(n>=10000000000 && n<100000000000)
       {
           cout<<10000<<" "<<100000<<" "<<n-1000000000<<endl;
       }
       else if(n>=100000000000 && n<1000000000000)
       {
           cout<<100000<<" "<<100000<<" "<<n-10000000000<<endl;
       }
       else if (n<=1000000000000 && n<10000000000000)
       {
           cout<<1000000<<" "<<100000<<" "<<n-100000000000<<endl;
       }
    }
}
